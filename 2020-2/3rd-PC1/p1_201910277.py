m = float(input("m:"))

if m >= 10.0:
    print("Cataclismo")
elif m >= 8.0:
    print("Gran")
elif m >= 7.0:
    print("Muy fuerte")
elif m >= 6.0:
    print("Fuerte")
elif m >= 5.0:
    print("Moderado")
elif m >= 4.0:
    print("Ligero")
elif m >= 3.0:
    print("Menor")
elif m >= 2.0:
    print("Muy menor")
else:
    print("Micro")